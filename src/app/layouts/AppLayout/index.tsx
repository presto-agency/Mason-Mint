import { FC, ReactNode, useEffect, useState } from 'react'
import Head from 'next/head'
import { withModal } from '@/context/modal'
import { Lenis as ReactLenis } from '@studio-freight/react-lenis'
import CustomCursor from '@/ui/CustomCursor/CustomCursor'
import useWindowDimensions from '@/hooks/useWindowDimensions'

type AppLayoutProps = {
  children: ReactNode
}

const AppLayout: FC<AppLayoutProps> = ({ children }) => {
  const [isTablet, setIsTablet] = useState(false)
  const { width } = useWindowDimensions()
  const options = {
    duration: 2.5,
    smoothWheel: true,
  }

  useEffect(() => {
    width <= 991 ? setIsTablet(true) : setIsTablet(false)
  }, [width])

  return (
    <>
      <Head>
        <title>Mason Mint</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      {!isTablet && <CustomCursor />}
      <ReactLenis root options={{ ...options }}>
        {children}
      </ReactLenis>
    </>
  )
}

export default withModal(AppLayout)
